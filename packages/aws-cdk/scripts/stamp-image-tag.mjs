#!/usr/bin/env node

import { execFileSync } from "node:child_process";
import { readFile, writeFile } from "node:fs/promises";
import { resolve } from "node:path";
import { fileURLToPath } from "node:url";

const targetFile = resolve(fileURLToPath(new URL("../src/pinned-service-image-tag.ts", import.meta.url)));
const tagArgPrefix = "--tag=";
const tagArg = process.argv.find((arg) => arg.startsWith(tagArgPrefix));
const dryRun = process.argv.includes("--dry-run");

function resolveMainShaFallback() {
  for (const ref of ["origin/main", "main"]) {
    try {
      const sha = execFileSync("git", ["rev-parse", "--verify", ref], {
        encoding: "utf8",
        stdio: ["ignore", "pipe", "ignore"],
      }).trim();
      if (/^[A-Fa-f0-9]{40}$/.test(sha)) {
        return sha;
      }
    } catch {
      // Ignore missing refs and continue fallback chain.
    }
  }

  return "latest";
}

const imageTag =
  tagArg?.slice(tagArgPrefix.length) ||
  process.env.IMAGE_TAG ||
  process.env.GITHUB_SHA ||
  resolveMainShaFallback();

if (!imageTag) {
  throw new Error("Missing image tag. Pass --tag=<tag> or set IMAGE_TAG/GITHUB_SHA.");
}

if (!/^[A-Za-z0-9][A-Za-z0-9._-]*$/.test(imageTag)) {
  throw new Error(`Invalid image tag: ${imageTag}`);
}

const updated = `/**
 * GHCR image tag shared by backend, worker, UI, codesearch, and migrate tasks.
 * Intentionally not configurable via {@link CtxPipeProps} so CDK-defined infra
 * cannot drift from the container images this construct expects.
 */
// Generated by release/build automation.
export const PINNED_SERVICE_IMAGE_TAG = "${imageTag}" as const;
`;

let currentTag = "(missing)";
try {
  const file = await readFile(targetFile, "utf8");
  const pattern = /PINNED_SERVICE_IMAGE_TAG\s*=\s*"([^"]+)"/m;
  currentTag = file.match(pattern)?.[1] ?? "(unknown)";
  if (file === updated) {
    console.log(`PINNED_SERVICE_IMAGE_TAG already set to ${imageTag}`);
    process.exit(0);
  }
} catch (error) {
  if (!(error instanceof Error) || !("code" in error) || error.code !== "ENOENT") {
    throw error;
  }
}

if (dryRun) {
  console.log(`Dry run: would set PINNED_SERVICE_IMAGE_TAG from ${currentTag} to ${imageTag}`);
  process.exit(0);
}

await writeFile(targetFile, updated);
console.log(`Set PINNED_SERVICE_IMAGE_TAG from ${currentTag} to ${imageTag}`);
